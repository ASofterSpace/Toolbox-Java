package com.asofterspace.toolbox.pdf;


public class PdfBuilderPage {

	private int width;

	private int height;


	public PdfBuilderPage(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

}
